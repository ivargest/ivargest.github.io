				<div class="contenedorapartados" id="jsvariables">
					<div class="titulopordefecto">
						<h3>Constantes y Variables</h3>
					</div>
					<div class="apartado">
						<p>
							En Javascript se trabaja principalmente fijando variables y constantes y dándoles valores con los que poder realizar funciones.
							<br><br>
							La diferencia entre una constante y una variable, como su nombre indica, es que las constantes no pueden cambiar de valor una vez declarados, mientras que las variables si se pueden cambiar.
							<br><br>
							Un buen ejemplo de esto sería la fecha de cumpleaños y la edad. Mientras la fecha de tu cumpleaños siempre será la misma, la edad va variando.
							<br><br>
							Para declarar las funciones se utiliza la siguiente sintaxis:
							<br>
							<div class="visualstudio">
								<p>
									<span class="variablescolor">var</span> x<span class="etiqueta">=</span> "valor de la variable";<span class="comentario">&emsp;//&emsp;Declara una variable</span>
									<br><br>
									<span class="variablescolor">const</span> y<span class="etiqueta">=</span> "valor de la constante";<span class="comentario">&emsp;//&emsp;Declara una constante</span>
								</p>
							</div>
							<br>
							Por regla general se utilizan sobre todo variables ya que son mas funcionales.
							<br><br>
							El scope es el ámbito en el que una variable va a ejercer su función.
							<br><br>
							Las variables pueden ser globales o locales.
							<br><br>
							Si se declara la variable fuera de la función será global y por lo tanto se podrá hacer uso de ella en todo el documento. Es la opción mas recomendable.
							<br><br>
							Si por el contrario se declara la variable dentro de una función, solo se podrá ejercer en esta misma.
						</p>
					</div>
				</div>
				<div class="contenedorapartados" id="jsoperadores">
					<div class="titulopordefecto">
						<h3>Operadores</h3>
					</div>
					<div class="apartado">
						<p>
							Los operadores son los que van a permitir dar y cambiar de valor a una variable.
							<br><br>
							Según su función hay diferentes tipos de operadores.
							<br><br>
							Los operadores aritméticos son los que se utilizan para operaciones numéricas:
							<br>
							<div class="visualstudio">
								<p>
									<span class="etiqueta">+</span><span class="comentario">&emsp;//&emsp;Sirve para sumar variables.</span>
									<br><br>
									<span class="etiqueta">-</span><span class="comentario">&emsp;//&emsp;Sirve para restar variables.</span>
									<br><br>
									<span class="etiqueta">*</span><span class="comentario">&emsp;//&emsp;Sirve para multiplicar variables.</span>
									<br><br>
									<span class="etiqueta">/</span><span class="comentario">&emsp;//&emsp;Sirve para dividir variables.</span>
									<br><br>
									<span class="etiqueta">%</span><span class="comentario">&emsp;//&emsp;Ofrece el valor sobrante de una división al evitar decimales.</span>
									</p>
							</div>
							<br>
							Los operadores condicionales permiten fijar condiciones para que se ejecute una función.
							<br><br>
							<div class="visualstudio">
								<p>
									<span class="etiqueta">==</span><span class="comentario">&emsp;//&emsp;El valor debe ser igual a</span>
									<br><br>
									<span class="etiqueta">===</span><span class="comentario">&emsp;//&emsp;El valor debe ser idéntico a (1 es igual a 1.0 pero no es idéntico)</span>
									<br><br>
									<span class="etiqueta">&#60;</span><span class="comentario">&emsp;//&emsp;El valor debe ser menor que</span>
									<br><br>
									<span class="etiqueta">&#62;</span><span class="comentario">&emsp;//&emsp;El valor debe ser mayor que</span>
									<br><br>
									<span class="etiqueta">&#60;</span><span class="comentario">&emsp;//&emsp;El valor debe ser menor o igual que</span>
									<br><br>
									<span class="etiqueta">&#62;</span><span class="comentario">&emsp;//&emsp;El valor debe ser mayor o igual que</span>
									<br><br>
									<span class="etiqueta">!=</span><span class="comentario">&emsp;//&emsp;El valor debe ser distinto que</span>
									<br><br>
									<span class="etiqueta">!==</span><span class="comentario">&emsp;//&emsp;El valor no debe ser idéntico a</span>
									</p>
							</div>
							<br>
							Los operadores lógicos permiten establecer mas de una condición a una función.
							<br><br>
							<div class="visualstudio">
								<p>
									&&<span class="comentario">&emsp;//&emsp;Permite añadir una condición. Para que se ejecute la función deben darse todas las condiciones.</span>
									<br><br>
									||<span class="comentario">&emsp;//&emsp;Permite añadir una condición también, pero al contrario que la anterior, la función se ejecutará si se da cualquiera de las condiciones.</span>
									<br><br>
									XOR<span class="comentario">&emsp;//&emsp;Permite añadir una condición pero solo puede darse una de ellas. Si se dan todas no se ejecutará la función.</span>
									</p>
							</div>
							<br>
							Los operadores de asignación dan valores inicialmente no aritméticos a las variables. Se utilizan principalmente para cadenas de texto.
							<br><br>
							<div class="visualstudio">
								<p>
									<span class="etiqueta">=</span><span class="comentario">&emsp;//&emsp;Asigna un valor</span>
