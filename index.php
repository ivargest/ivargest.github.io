							<div class="visualstudio">
								<p>
									&#60;<span class="etiqueta">body</span>&#62;
									<br><br>
									&emsp;&emsp;&#60;<span class="etiqueta">script</span> <span class="atributo">type</span>="text/javascript" <span class="atributo">src</span>="assets/js/script.js"&#62;&#60;/<span class="etiqueta">script</span>&#62;
